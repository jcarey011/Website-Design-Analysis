package edu.odu.cs.cs350;
/**
 * Anchor tag TargetType options
 * {@link #HTMLDocument}
 * {@link #Archive}
 * {@link #Video}
 * {@link #Audio}
 */
public enum TargetType {
	/**
	 * Tag links to an HTML document
	 */
	HTMLDocument,
	/**
	 * Tag links to an archive
	 */
	Archive,
	/**
	 * Tag links to a video
	 */
	Video,
	/**
	 * Tag links to an audio file
	 */
	Audio
}
